def floyd(graph):
    b = [[sys.maxsize for i in range(len(graph))] for j in range(len(graph))]
    for i in range(len(graph)):
        for j in range(len(graph)):
            if i == j:
                b[i][j] = 0
            if graph[i][j] > 0:
                b[i][j] = graph[i][j]
    for k in range(len(graph)):
        for i in range(len(graph)):
            for j in range(len(graph)):
                b[i][j] = min(b[i][j], b[i][k] + b[k][j])

    return b

